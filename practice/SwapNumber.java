package practice;

public class SwapNumber {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 0;
        System.out.println("Before Swapping "+ a+ "\t"+ b);
        c=a;
        a=b;
        b=c;
        System.out.println("After Swapping "+ a+ "\t"+ b);
    }
}
